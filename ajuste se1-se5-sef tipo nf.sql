SELECT SE1010.* 
UPDATE SE1020 SET E1_TIPO='NF'
FROM SF2020 
INNER JOIN SE1020 ON F2_FILIAL=E1_FILIAL AND F2_DOC=E1_NUM AND F2_SERIE=E1_PREFIXO AND F2_CLIENTE=E1_CLIENTE AND F2_LOJA=E1_LOJA AND SE1020.D_E_L_E_T_=''
INNER JOIN SA1010 ON E1_CLIENTE=A1_COD AND E1_LOJA=A1_LOJA AND SA1010.D_E_L_E_T_=''
WHERE SF2020.D_E_L_E_T_='' AND F2_ESPECIE<>E1_TIPO AND F2_ESPECIE<>'SPED' AND A1_XPE='S'


