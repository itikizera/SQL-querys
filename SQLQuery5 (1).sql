SELECT RR_FILIAL, RR_MAT, MAX(RR_VALOR), RR_DATAPAG, RR_CC, RR.R_E_C_N_O_, MIN(RC1.RC1_NUMTIT) FROM SRR010 RR
RIGHT JOIN RC1010 RC1 ON RR.RR_FILIAL = RC1.RC1_FILTIT AND RR.RR_DATAPAG = RC1.RC1_VENCTO --AND RR.RR_VALOR = RC1.RC1_VALOR
WHERE RR.D_E_L_E_T_=''
AND RR_PD = '693'
AND RR_FILIAL BETWEEN '001NE001' AND '001NE001'
AND RR_DATAPAG BETWEEN '20140414' AND '20140414'
AND 
(RC1_CODTIT = '007'
AND RC1_VENCTO BETWEEN '20140414' AND '20140414'
AND RC1_INTEGR = '1'
AND RC1_FILTIT BETWEEN '001NE001' AND '001NE001'
AND RC1.D_E_L_E_T_='')
GROUP BY RR_FILIAL, RR_MAT, RR_DATAPAG, RR_CC, RR.R_E_C_N_O_, RC1.R_E_C_N_O_
