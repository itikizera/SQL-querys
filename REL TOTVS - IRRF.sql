INNER JOIN SRD010 RD ON RE.RE_FILIALP=RD.RD_FILIAL AND RE.RE_MATP=RD.RD_MAT AND RE.RE_CCP=RD.RD_CC
INNER JOIN SRA010 RA ON RA.RA_FILIAL=RD.RD_FILIAL AND RA.RA_MAT=RD.RD_MAT AND RA.RA_CC=RD.RD_CC
WHERE RE_FILIALD = '001NE001'
AND SUBSTRING(RE.RE_DATA,1,6)='201405'
AND RD_PD IN ('751','731')
AND RD_DATARQ = '201405'
--AND RA.RA_SITFOLH='T'
--AND RE_CCD = '020000003'
