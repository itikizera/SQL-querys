SELECT RD_MAT, RD_PD, RD_CC, SUM(RD_VALOR) FROM SRD010 RD
INNER JOIN SRA010 RA ON RA.RA_FILIAL=RD.RD_FILIAL AND RA.RA_MAT=RD.RD_MAT AND RA.RA_CC=RD.RD_CC
WHERE 
RD_PD IN ('751','731')
AND RD_FILIAL = '001NE001'
AND RD_DATARQ = '201405'
AND RD.D_E_L_E_T_=''
--AND RD_CC = '020000003'
AND RA.RA_SITFOLH<>'D'
GROUP BY RD_MAT, RD_PD, RD_CC
UNION ALL
