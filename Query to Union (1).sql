SELECT E1_FILIAL,  	
E1_EMISSAO,  	
E1_PREFIXO,  	
E1_NUM,  		
E1_CLIENTE,  	
E1_LOJA,  		
A1_NOME, 		
A1_CGC, 		
F2_VALBRUT, 	
E1_VALOR, 
E1_PARCELA, 
TIT_PIS_VAL = (SELECT E1_VALOR FROM  SE1010 IE1 WHERE IE1.D_E_L_E_T_='' AND IE1.E1_FILIAL = E1X.E1_FILIAL AND IE1.R_E_C_N_O_=E1X.R_E_C_N_O_ AND
	 IE1.E1_PARCELA=E1X.E1_PARCELA AND IE1.E1_TIPO = 'PI-' AND IE1.E1_PREFIXO = E1X.E1_PREFIXO AND IE1.E1_NUM = E1X.E1_NUM AND IE1.E1_CLIENTE = E1X.E1_CLIENTE), 
TIT_COF_VAL = (SELECT E1_VALOR FROM  SE1010 IE1 WHERE IE1.D_E_L_E_T_='' AND IE1.E1_FILIAL = E1X.E1_FILIAL AND IE1.R_E_C_N_O_=E1X.R_E_C_N_O_ AND 
	 IE1.E1_PARCELA=E1X.E1_PARCELA AND IE1.E1_TIPO = 'CF-' AND IE1.E1_PREFIXO = E1X.E1_PREFIXO AND IE1.E1_NUM = E1X.E1_NUM AND IE1.E1_CLIENTE = E1X.E1_CLIENTE),
TIT_CSLL_VAL = (SELECT E1_VALOR FROM  SE1010 IE1 WHERE IE1.D_E_L_E_T_='' AND IE1.E1_FILIAL = E1X.E1_FILIAL AND IE1.R_E_C_N_O_=E1X.R_E_C_N_O_ AND 
	 IE1.E1_PARCELA=E1X.E1_PARCELA AND IE1.E1_TIPO = 'CS-' AND IE1.E1_PREFIXO = E1X.E1_PREFIXO AND IE1.E1_NUM = E1X.E1_NUM AND IE1.E1_CLIENTE = E1X.E1_CLIENTE),
E1X.R_E_C_N_O_ FROM SE1010 E1X
INNER JOIN SA1010 A1 WITH(NOLOCK) 
	ON A1.D_E_L_E_T_='' 	
	AND E1_CLIENTE = A1_COD
	AND E1_LOJA = A1_LOJA  	
INNER JOIN SF2010 F2 WITH(NOLOCK) 
	ON F2.D_E_L_E_T_='' 	
	AND E1_FILIAL = F2_FILIAL 
	AND E1_PREFIXO = F2_PREFIXO 
	AND E1_NUM = F2_DOC  		
	AND E1_CLIENTE = F2_CLIENTE 
	AND E1_LOJA = F2_LOJA  	
WHERE E1X.E1_BAIXA BETWEEN  '20140723' AND '20140723'
AND E1X.E1_FILIAL BETWEEN  '001NE001' AND '001NE001'
AND E1X.E1_CLIENTE BETWEEN  '' AND 'ZZZZZZ'
AND E1X.E1_LOJA BETWEEN  '' AND 'ZZ'
AND E1_TIPO = 'NF'
GROUP BY E1_FILIAL,  	
E1_EMISSAO,  	
E1_PREFIXO,  	
E1_NUM,  		
E1_CLIENTE,  	
E1_LOJA,  		
A1_NOME, 		
A1_CGC, 		
F2_VALBRUT, 	
E1_VALOR,
E1X.R_E_C_N_O_,
E1_PARCELA